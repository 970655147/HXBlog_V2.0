package com.hx.blog_v2.domain.vo;

/**
 * id -> name
 *
 * @author Jerry.X.He <970655147@qq.com>
 * @version 1.0
 * @date 6/11/2017 2:55 PM
 */
public class Id2NameVO {

    private String id;
    private String name;

    public Id2NameVO() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
